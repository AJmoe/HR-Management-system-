    private String ValidateFormData(boolean isSaving) throws Exception{
            String msg = "";
            if(txtEmployeeID.getText().equals("")){               msg = "Please enter the employee id before proceeding !!";   
            }else if(txtDateHired.getText().equals("")){          msg = "Please enter the employee date hired before proceeding !!"; 
            }else if(txtFirstname.getText().equals("")){          msg = "Please enter the employee firstname before proceeding !!"; 
            }else if(txtLastname.getText().equals("")){           msg = "Please enter the employee lastname before proceeding !!"; 
            }else if(cboGender.getSelectedIndex()==0){            msg = "Please select the employee gender before proceeding !!"; 
            }else if(txtDOB.getText().equals("")){                msg = "Please enter the employee DOB before proceeding !!"; 
            }else if(txtAddress.getText().equals("")){            msg = "Please enter the employee address before proceeding !!"; 
            }else if(cboCity.getSelectedIndex()==0){              msg = "Please select the employee city before proceeding !!"; 
            }else if(cboPosition.getSelectedIndex()==0){          msg = "Please select the employee position before proceeding !!"; 
            }else if(cboDepartment.getSelectedIndex()==0){        msg = "Please select the employee department before proceeding !!"; 
            }else if(txtHoursWorked.getText().equals("")){        msg = "Please enter the employee hours worked before proceeding !!"; 
            }else if(txtPayRate.getText().equals("")){            msg = "Please enter the employee pay rate before proceeding !!"; 
            }else if(txtLeaveDays.getText().equals("")){          msg = "Please enter the employee leave days before proceeding !!"; 
            }else if(txtCarAllowance.getText().equals("")){       msg = "Please enter the employee car allowance before proceeding !!"; 
            }else if(txtMonthlyGratuity.getText().equals("")){    msg = "Please enter the employee gratuity before proceeding !!"; 
            }else if(txtTaxRate.getText().equals("")){            msg = "Please enter the employee tax rate before proceeding !!"; 
            }
            if(isSaving==true){ //only validate these when saving
                if(txtTotalAllowances.getText().equals("")){    msg = "Please click the calculate button to calculate pay before proceeding !!"; 
                }else if(txtTotalDeductions.getText().equals("")){    msg = "Please click the calculate button to calculate pay before proceeding !!"; 
                }else if(txtGrossPay.getText().equals("")){           msg = "Please click the calculate button to calculate pay before proceeding !!"; 
                }else if(txtNetPay.getText().equals("")){             msg = "Please click the calculate button to calculate pay before proceeding !!"; 
                }
            }
            
            return msg;
        }

