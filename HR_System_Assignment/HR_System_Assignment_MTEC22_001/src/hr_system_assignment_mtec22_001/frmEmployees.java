    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try{
           //validadatate
            String msg = ValidateFormData(true);
            if(msg.trim().length()>0){
                JOptionPane.showMessageDialog(this, msg, mgr.GetSystemTitle(), JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            //save details
            String[] data = {   txtEmployeeID.getText(),
                                txtFirstname.getText(),
                                txtLastname.getText(),
                                cboGender.getSelectedItem().toString(),
                                txtDOB.getText(),
                                txtAddress.getText(),
                                cboCity.getSelectedItem().toString(),
                                cboPosition.getSelectedItem().toString(),
                                cboDepartment.getSelectedItem().toString(),
                                txtHoursWorked.getText(),
                                txtPayRate.getText(),
                                txtLeaveDays.getText(),
                                txtCarAllowance.getText(),
                                txtMonthlyGratuity.getText(),
                                txtTaxRate.getText(),
                                txtTotalAllowances.getText(),
                                txtTotalDeductions.getText(),
                                txtGrossPay.getText(),
                                txtNetPay.getText(),
                                txtDateHired.getText()
                            };
            int rows = mgr.WriteFileData("employees.txt", data);
            
            //confirm 
            if(rows > 0){
                JOptionPane.showMessageDialog(this, "Employee pay details saved successfully !!", mgr.GetSystemTitle(), JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Employee pay details saving failed !!", mgr.GetSystemTitle(), JOptionPane.WARNING_MESSAGE);
            }
            //
           
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex, mgr.GetSystemTitle(), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        try{
           //validadatate
            String msg = ValidateFormData(false);
            if(msg.trim().length()>0){
                JOptionPane.showMessageDialog(this, msg, mgr.GetSystemTitle(), JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            //total amount  : hours  rate
            double hoursworked = Double.parseDouble(txtHoursWorked.getText());
            double payrate = Double.parseDouble(txtPayRate.getText());
            double payamount = hoursworked * payrate;
            
            //calculate pay
            double carallowancerate = Double.parseDouble(txtCarAllowance.getText());
            double carallowance = carallowancerate / 100 * payamount;
            
            //gross pay
            double grosspay = payamount + carallowance;
            
            //deductions
            double taxrate = Integer.parseInt(txtTaxRate.getText());
            double taxamount = taxrate/100 * payamount;
            
            //net pay
            double netpay = grosspay - taxamount;
            
            //set values | String.valueOf(value); //change to string
            txtTotalAllowances.setText( RoundOffTo2DecPlaces(carallowance));
            txtTotalDeductions.setText(RoundOffTo2DecPlaces(taxamount));
            txtGrossPay.setText(RoundOffTo2DecPlaces(grosspay));
            txtNetPay.setText(RoundOffTo2DecPlaces(netpay));
            
            //
           
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex, mgr.GetSystemTitle(), JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCalculateActionPerformed

